#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;
	int		l;

	i = 0;
	l = 0;
	while (s1[l])
		l++;
	l++;
	str = (char *)malloc(sizeof(char) * l);
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
